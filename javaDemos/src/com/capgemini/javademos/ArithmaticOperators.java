package com.capgemini.javademos;
//program for arithmatic operator
public class ArithmaticOperators {

	public static void main(String[] args) {   //main method
		int x = 10, y=20;                      //declaring variable
		System.out.println(x+y);               //printing addition
		System.out.println(--x);               //printing pre increment of x
		System.out.println(++y);               //printing pre increment of y


	}

}

package com.capgemini.javademos;
//program for ternary operator
public class TernaryOperator {

	public static void main(String[] args) {  //main method
        int x = 10;                           //declaring variable
		
		x = (10 == x) ? 1 : 0;                //condition true or false
		
		System.out.println(x);                //printing value
     


	}

}
